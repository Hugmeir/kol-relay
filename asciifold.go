import (
    "unicode/utf8"
)

func AsciiFold(s string) string {
    for i, w := 0, 0; i < len(s); i += w {
        c, width := utf8.DecodeRuneInString(s[i:])
        w = width
        fold, ok := asciiFold[c]
        if ok {
            s = s[:i] + fold + s[i+w:]
            w = len(fold)
        }
    }
    return s
}

